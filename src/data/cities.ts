// Utility functions
export function getCitiesByCountry(countryCode: string): City[] {
  return cities.filter(city => city.countryCode === countryCode);
}

export function searchCities(query: string): City[] {
  const lowercaseQuery = query.toLowerCase().trim();
  return cities.filter(city => 
    city.name.toLowerCase().includes(lowercaseQuery) ||
    city.country.toLowerCase().includes(lowercaseQuery) ||
    city.stateCode.toLowerCase().includes(lowercaseQuery) ||
    city.region.toLowerCase().includes(lowercaseQuery) ||
    `${city.name.toLowerCase()}, ${city.stateCode.toLowerCase()}`.includes(lowercaseQuery) ||
    `${city.name.toLowerCase()}, ${city.country.toLowerCase()}`.includes(lowercaseQuery)
  );
}

export function getCityById(id: string): City | undefined {
  return cities.find(city => city.id === id);
}

export function getCitiesByTimezone(timezone: string): City[] {
  return cities.filter(city => city.timezone === timezone);
}

export function getUniqueTimezones(): string[] {
  return [...new Set(cities.map(city => city.timezone))];
}

export function getNearestCities(latitude: number, longitude: number, limit: number = 5): City[] {
  return cities
    .map(city => ({
      ...city,
      distance: calculateDistance(latitude, longitude, city.latitude, city.longitude)
    }))
    .sort((a, b) => a.distance - b.distance)
    .slice(0, limit);
}

function calculateDistance(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371; // Earth's radius in km
  const dLat = toRad(lat2 - lat1);
  const dLon = toRad(lon2 - lon1);
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * 
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
}

function toRad(degrees: number): number {
  return degrees * (Math.PI / 180);
}
